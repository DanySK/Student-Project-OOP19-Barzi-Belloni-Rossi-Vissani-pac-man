package view;

/**
 * This enumeration represents the scene that can be loaded.
 */
public enum GameScene {
    /**
     * The Main Menu scene.
     */
    MAIN_MENU,
    /**
     * The game scene.
     */
    GAME,
    /**
     * The game over scene.
     */
    GAMEOVER,
    /**
     * The pause scene.
     */
    PAUSE,
    /**
     * The scene with the leader board.
     */
    LEADERBOARD,
    /**
     * The scene with the settings of the game.
     */
    SETTINGS,
    /**
     * The scene with the rules of the game.
     */
    RULES;

}
